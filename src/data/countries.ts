// Utility functions
export function getCountryByCode(code: string): Country | undefined {
  return countries.find(country => country.code === code);
}

export function getCountryByName(name: string): Country | undefined {
  return countries.find(country => country.name.toLowerCase() === name.toLowerCase());
}

export function getStatesByCountryCode(countryCode: string): { code: string, name: string }[] {
  const country = getCountryByCode(countryCode);
  if (!country || !country.states) return [];
  
  return Object.entries(country.states).map(([code, names]) => ({
    code,
    name: names[0]
  }));
} 